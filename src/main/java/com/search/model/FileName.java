package com.search.model;

public class FileName {

	private String name;
	
	public FileName() {
	}
	
	public FileName(String name) {
		this.name = name;
	}
	
	/**
	 * @return the fileName
	 */
	public String getFileName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setFileName(String name) {
		this.name = name;
	}
}
